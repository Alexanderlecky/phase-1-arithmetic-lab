// Multiplying variables
let num1 = 31;
let num2 = 2;
let multiply = num1 * num2;  // This will be 62

// Generating a random integer greater than 0
let random = Math.floor(Math.random() * 100) + 1;  // Generates a random integer between 1 and 100

// Calculating the remainder
let num3 = 14;
let num4 = 5;
let mod = num3 % num4;  // This will be 4

// Finding the highest number in a set
let numbers = [5, 10, 20, 3, 7];
let max = Math.max(...numbers);  // This will be 20

console.log("multiply:", multiply);
console.log("random:", random);
console.log("mod:", mod);
console.log("max:", max);
